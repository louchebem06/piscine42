#include <unistd.h>

void	ft_print_comb(void)
{
	char sec[3];

	sec[0] = '0';
	while (sec[0] <= '9')
	{
		sec[1] = sec[0] + 1;
		while (sec[1] <= '9')
		{
			sec[2] = sec[1] + 1;
			while (sec[2] <= '9')
			{
				write(1, &sec[0], 3);
				if (sec[0] != '7')
					write(1, ", ", 2);
				sec[2]++;
			}
			sec[1]++;
		}
		sec[0]++;
	}
}
