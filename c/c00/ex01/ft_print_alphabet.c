#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	int ascii;

	ascii = 97;
	while (ascii != 123)
	{
		ft_putchar(ascii);
		ascii++;
	}
}
