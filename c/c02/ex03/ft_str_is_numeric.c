int	ft_str_is_numeric(char *str)
{
	int i;
	int check;

	i = 0;
	check = 1;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			check = 1;
		else
			return (0);
		i++;
	}
	return (check);
}
