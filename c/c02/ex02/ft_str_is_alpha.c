int	ft_str_is_alpha(char *str)
{
	int i;
	int check;

	i = 0;
	check = 1;
	while (str[i] != '\0')
	{
		if ((str[i] == 32) ||
			(str[i] >= 65 && str[i] <= 90) ||
			(str[i] >= 97 && str[i] <= 122))
			check = 1;
		else
			check = 0;
		if (check == 0)
			break ;
		i++;
	}
	return (check);
}
