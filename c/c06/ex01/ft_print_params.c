#include <unistd.h>

int	main(int ac, char **av)
{
	int j;
	int i;

	j = 1;
	i = 0;
	while (j != ac)
	{
		if (av[j][i] == '\0')
		{
			i = 0;
			j++;
			write(1, "\n", 1);
		}
		else
		{
			write(1, &av[j][i], 1);
			i++;
		}
	}
	return (0);
}
