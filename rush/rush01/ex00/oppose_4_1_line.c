#include "biblio.h"

void	oppose_4_1_line(int map[6][6], int *error)
{
	if (map[1][0] == 4 && map[1][5] != 1)
		*error = 1;
	if (map[2][0] == 4 && map[2][5] != 1)
		*error = 1;
	if (map[3][0] == 4 && map[3][5] != 1)
		*error = 1;
	if (map[4][0] == 4 && map[4][5] != 1)
		*error = 1;
	if (map[1][5] == 4 && map[1][0] != 1)
		*error = 1;
	if (map[2][5] == 4 && map[2][0] != 1)
		*error = 1;
	if (map[3][5] == 4 && map[2][0] != 1)
		*error = 1;
	if (map[4][5] == 4 && map[4][0] != 1)
		*error = 1;
}
