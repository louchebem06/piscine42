#include "biblio.h"

void	ft_remplissage(int map[6][6])
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (j < 6)
	{
		map[j][i] = 0;
		i++;
		if (i == 6)
		{
			i = 0;
			j++;
		}
	}
}
