#include "biblio.h"

void	face_inverse(int map[6][6], int *error)
{
	if (map[0][1] != 2 && map[0][1] == map[5][1])
		*error = 1;
	if (map[0][2] != 2 && map[0][2] == map[5][2])
		*error = 1;
	if (map[0][3] != 2 && map[0][3] == map[5][3])
		*error = 1;
	if (map[0][4] != 2 && map[0][4] == map[5][4])
		*error = 1;
	if (map[1][0] != 2 && map[1][0] == map[1][5])
		*error = 1;
	if (map[2][0] != 2 && map[2][0] == map[2][5])
		*error = 1;
	if (map[3][0] != 2 && map[3][0] == map[3][5])
		*error = 1;
	if (map[4][0] != 2 && map[4][0] == map[4][5])
		*error = 1;
}
