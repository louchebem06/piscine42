#include "biblio.h"

void	if_2_1_3004_col(int map[6][6])
{
	int j;
	int i;

	j = 0;
	i = 1;
	while (i < 5)
	{
		if (map[j][i] == 2 && map[j + 5][i] == 1)
		{
			map[j + 1][i] = 3;
			map[j + 4][i] = 4;
		}
		if (map[j][i] == 1 && map[j + 5][i] == 2)
		{
			map[j + 1][i] = 4;
			map[j + 4][i] = 3;
		}
		i++;
	}
}

void	if_2_1_3004_lig(int map[6][6])
{
	int j;
	int i;

	j = 1;
	i = 0;
	while (j < 5)
	{
		if (map[j][i] == 2 && map[j][i + 5] == 1)
		{
			map[j][i + 1] = 3;
			map[j][i + 4] = 4;
		}
		if (map[j][i] == 1 && map[j][i + 5] == 2)
		{
			map[j][i + 1] = 4;
			map[j][i + 4] = 3;
		}
		j++;
	}
}

void	if_2_1_3004(int map[6][6])
{
	if_2_1_3004_col(map);
	if_2_1_3004_lig(map);
}
