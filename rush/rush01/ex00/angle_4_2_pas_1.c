#include "biblio.h"

void	angle_4_2_pas_1(int map[6][6], int *error)
{
	if (map[0][1] == 4 && map[1][0] == 2 && map[1][5] == 1)
		*error = 1;
	if (map[1][0] == 4 && map[0][1] == 2 && map[5][1] == 1)
		*error = 1;
	if (map[0][4] == 4 && map[1][5] == 2 && map[1][0] == 1)
		*error = 1;
	if (map[1][5] == 4 && map[0][4] == 2 && map[5][4] == 1)
		*error = 1;
	if (map[4][0] == 4 && map[5][1] == 2 && map[0][1] == 1)
		*error = 1;
	if (map[5][1] == 4 && map[4][0] == 2 && map[4][5] == 1)
		*error = 1;
	if (map[4][5] == 4 && map[5][4] == 2 && map[0][4] == 1)
		*error = 1;
	if (map[5][4] == 4 && map[4][5] == 2 && map[4][0] == 1)
		*error = 1;
}
